/**
     * Event handler untuk tombol "Bayar".
     * Melakukan validasi saldo dan update status jika pembayaran berhasil.
     *
     * @param evt event klik tombol bayar
     */
    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
    try {
        int saldoUser = Integer.parseInt(isiSaldo.getText());
        
        if (saldoUser == totalHarga) {
            updateStatusPesanan();
            javax.swing.JOptionPane.showMessageDialog(this, "Pembayaran berhasil! Pesanan lunas.");
            dispose();
        } else if (saldoUser > totalHarga) {
            int kembalian = saldoUser - totalHarga;
            updateStatusPesanan();
            javax.swing.JOptionPane.showMessageDialog(this, "Pembayaran berhasil! Kembalian: Rp " + kembalian);
            dispose();
        } else {
            int kekurangan = totalHarga - saldoUser;
            javax.swing.JOptionPane.showMessageDialog(this, "Saldo kurang Rp " + kekurangan + ". Tidak boleh hutang.");
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Input saldo tidak valid!");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }    }//GEN-LAST:event_btnBayarActionPerformed

    /**
     * Memperbarui status pesanan di database menjadi "Sedang Dicuci".
     */
    private void updateStatusPesanan() {
    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/autoclean_javadesk", "root", "");
        Statement stmt = conn.createStatement();

        String sql = "UPDATE pelanggan SET status = 'Sedang Dicuci' " +
             "WHERE username = '" + username + "' " +
             "AND plat_kendaraan = '" + platKendaraan + "' " +
             "AND jadwal_cuci = '" + jadwalCuci + "' " +
             "AND status != 'Sedang Dicuci'";

        stmt.executeUpdate(sql);

        stmt.close();
        conn.close();
    } catch (Exception e) {
        System.out.println("Error update status: " + e.getMessage());
    }
}

    
    
